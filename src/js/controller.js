const controlRecipes = async function () {
  try {
    const id = window.location.hash.slice(1); // slice the # from the string
    // console.log(id);

    if (!id) return; // if id does not exist return immediately
    recipeView.renderSpinnner();

    // 0) update results view to mark selected search result
    resultsView.update(model.getSearchResultPage());

    // updating bookmark view
    bookmarksView.update(model.state.bookmarks);

    // 1) loading recipe
    await model.loadRecipe(id);

    // 2) rendering recipe
    recipeView.render(model.state.recipe);
  } catch (err) {
    recipeView.renderError();
  }
};

const controlSearchResults = async function () {
  try {
    resultsView.renderSpinnner();

    // 1) get search query
    const query = searchView.getQuery();
    if (!query) return;

    // 2) load search results
    await model.loadSearchResults(query);

    // 3) render results
    // console.log(model.state.search.results);
    // resultsView.render(model.state.search.results);

    resultsView.render(model.getSearchResultPage());

    // 4) render initial pagination results
    paginationView.render(model.state.search);
  } catch (err) {
    console.error(err);
  }
};

